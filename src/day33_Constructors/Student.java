package day33_Constructors;

public class Student {
	String name;
	int age;
	char gender;
	int year;
	String course;
	String university;

	public Student(String name, int age, char gender, int year, String course, String university) {

		this.name = name;
		this.age = age;
		this.gender = gender;
		this.year = year;
		this.course = course;
		this.university = university;
	}
	
}
